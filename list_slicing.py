l1 = [10,20,30,40,50,60,10,20,30,40,50]

# Indexing
print(l1[-1])  # 60


# Slicing 

# print(l1[start : end (n-1) : step (n-1)])

print(l1[1:5:1])   # [20,30,40,50]

print(l1[3:6:2])  # [40, 60]

print(l1[6:2:1])  # []

print(l1[-7:-1:2]) # [50, 10, 30]

print(l1[-2:-8:-3])  # [40, 10]

l1 = [10,20,30,40,50,60,10,20,30,40,50]

print(l1[::])   # [10, 20, 30, 40, 50, 60, 10, 20, 30, 40, 50]

print(l1[::-3])  # [50, 20, 50, 20]

print(l1[:-4:])   # [10, 20, 30, 40, 50, 60, 10]

print(l1[-2::])  # [40,50]

print(l1[3:-3:2])  # [40, 60, 20]

print(l1[-3:5:2])   # []